package ex_09_Switch_Condition;

public class Lab71_Switch_P6 {
    public static void main(String[] args) {
        char code ='C';
        switch (code)
        {
            default:
                System.out.println("Hellooo");
                break;
            case 'A':
                System.out.println("65");
                break;
            case 'B':
                System.out.println("66");
                break;
        }
    }
}
