package ex_11_While;

public class Lab84_while_P1 {
    public static void main(String[] args) {
       int i=0; //initilization
        // WHILE -> checks condition and then prints the stmt
       while(i<10) //condition
       {
           System.out.println(i); // print stmt
           i++; //increment / decrement
       }
    }
}
