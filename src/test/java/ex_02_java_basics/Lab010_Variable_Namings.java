package ex_02_java_basics;

public class Lab010_Variable_Namings {
    public static void main(String[] args) {
        int _q2=23; //variable name can start with underscore
        int $ = 30; // variable name can start with $
        int $ab_c = 65;
        //int 123 = 76; //variable name cannot begin with numbers
        boolean isOff = true;
        boolean isOn = false;
        System.out.println(_q2);
        System.out.println($);
        System.out.println($ab_c);
        System.out.println(isOff);
        System.out.println(isOn);

    }
}
