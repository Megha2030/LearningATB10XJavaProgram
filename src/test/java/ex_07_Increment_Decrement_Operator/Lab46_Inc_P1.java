package ex_07_Increment_Decrement_Operator;

public class Lab46_Inc_P1 {
    public static void main(String[] args) {
        int a =10;
        int b = ++a;
        System.out.println(b);
        System.out.println(a);

        int c=20;
        System.out.println(c++);
        System.out.println(++c);
    }
}
