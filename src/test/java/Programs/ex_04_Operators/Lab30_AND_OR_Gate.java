package Programs.ex_04_Operators;

public class Lab30_AND_OR_Gate {
    public static void main(String[] args) {
        System.out.println(true && true);
        System.out.println(true && false);
        System.out.println(true && false);
        System.out.println(false && false);

        System.out.println(true || true);
        System.out.println(true || false);
        System.out.println(true || false);
        System.out.println(false || false);

    }
}
